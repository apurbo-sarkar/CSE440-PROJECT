a = 1e-3
for col in datasets:
    tokenized_data = [text.split() for text in combined_df[col].astype(str)]
    model = Word2Vec(vector_size=100, window=5, sg=1, min_count=5)
    model.build_vocab(tokenized_data)
    model.train(tokenized_data, total_examples=model.corpus_count, epochs=10)
    all_words = []
    for s in tokenized_data:
        for w in s:
            all_words.append(w)
    counts = Counter(all_words)
    total = sum(counts.values())
    vectors = []
    for tokens in tokenized_data:
        word_vecs = []
        for w in tokens:
            if w in model.wv:
                raw_vector = model.wv[w]
                word_probability = counts[w] / total
                weight = a / (a + word_probability)
                weighted_vector = raw_vector * weight
                word_vecs.append(weighted_vector)
        vectors.append(np.mean(word_vecs, axis=0) if word_vecs else np.zeros(100))
    X = np.array(vectors)
    skipgram_results[col] = X


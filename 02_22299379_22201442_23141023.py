def plot_topic_cooccurrence(df, topic, n_words=10):
    subset = df[df['News Topic'] == topic]['News Headline']
    vec = CountVectorizer(max_features=n_words)
    bow = vec.fit_transform(subset)
    matrix = (bow.T * bow).toarray()
    np.fill_diagonal(matrix, 0)
    names = vec.get_feature_names_out()
    df_matrix = pd.DataFrame(matrix, index=names, columns=names)
    plt.figure(figsize=(10, 6))
    sns.heatmap(df_matrix, annot=True, fmt='d', cmap="YlGnBu", cbar=False)
    plt.title(f"Word Co-occurrence: {topic} (Top {n_words} words)")
    plt.show()


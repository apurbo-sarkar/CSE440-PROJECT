combined_df['text_no_preprocessing'] = combined_df['News Headline']
stop_words = set(stopwords.words('english'))

stemmer = PorterStemmer()
def extreme_preprocessing_stem(text):
    text = html.unescape(text)
    text = re.sub(r'html.*?news headlines:', '', text, flags=re.IGNORECASE | re.DOTALL)
    text = re.sub(r'<.*?>', '', text)
    text = contractions.fix(text)
    text = re.sub(r'[A-Z\s\/]+(\(Reuters\)|\(AFP\)|\(AP\)|--|-)\s*', ' ', text)
    text = text.lower()
    text = re.sub(r'[^a-z\s]', ' ', text)
    tokens = [stemmer.stem(word) for word in text.split() if word not in stop_words]
    return ' '.join(tokens)
combined_df['text_extreme_stem'] = combined_df['News Headline'].apply(extreme_preprocessing_stem)

lemmatizer = WordNetLemmatizer()
def optimum_preprocessing(text):
    text = html.unescape(text)
    text = re.sub(r'html.*?news headlines:', '', text, flags=re.IGNORECASE | re.DOTALL)
    text = re.sub(r'<.*?>', '', text)
    text = contractions.fix(text)
    text = re.sub(r'[A-Z\s\/]+(\(Reuters\)|\(AFP\)|\(AP\)|--|-)\s*', ' ', text)
    text = re.sub(r'[\$\£]\d+', ' MONEY ', text)
    text = re.sub(r'\d+%', ' PERCENT ', text)
    text = re.sub(r'\d+\s?(mb|gb|km|mph)', ' UNIT ', text, flags=re.IGNORECASE)
    text = text.lower()
    text = re.sub(r"'s\b", "", text)
    text = re.sub(r'[^a-z0-9\s]', '', text)
    tokens = [word for word in text.split() if word not in stop_words]
    tokens = [lemmatizer.lemmatize(word) for word in tokens]
    return ' '.join(tokens)
combined_df['text_optimum'] = combined_df['News Headline'].apply(optimum_preprocessing)

